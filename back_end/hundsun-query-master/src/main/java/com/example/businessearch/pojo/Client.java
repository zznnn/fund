package com.example.businessearch.pojo;

public class Client {
    private String clientId;
    private String clientName;
    private String clientGender;
    private Integer clientAge;
    private String clientOrganFlag;
    private String clientIdKind;
    private String clientPhnumber;
    private String clientRisk;

    public String getClientId() {
        return clientId;
    }

    public void setClientId(String clientId) {
        this.clientId = clientId;
    }

    public String getClientName() {
        return clientName;
    }

    public void setClientName(String clientName) {
        this.clientName = clientName;
    }

    public String getClientGender() {
        return clientGender;
    }

    public void setClientGender(String clientGender) {
        this.clientGender = clientGender;
    }

    public Integer getClientAge() {
        return clientAge;
    }

    public void setClientAge(Integer clientAge) {
        this.clientAge = clientAge;
    }

    public String getClientOrganFlag() {
        return clientOrganFlag;
    }

    public void setClientOrganFlag(String clientOrganFlag) {
        this.clientOrganFlag = clientOrganFlag;
    }

    public String getClientIdKind() {
        return clientIdKind;
    }

    public void setClientIdKind(String clientIdKind) {
        this.clientIdKind = clientIdKind;
    }

    public String getClientPhnumber() {
        return clientPhnumber;
    }

    public void setClientPhnumber(String clientPhnumber) {
        this.clientPhnumber = clientPhnumber;
    }

    public String getClientRisk() {
        return clientRisk;
    }

    public void setClientRisk(String clientRisk) {
        this.clientRisk = clientRisk;
    }
}
